/**
 * SEO-подзадачи на месяц. vars: { pages, monthLabel, region, servicesUrl, direction }
 * Возвращает массив { title, description, bbcode, responsibleId }.
 */
export function seoSubtasks({ pages, monthLabel, region = DEFAULT_REGION, servicesUrl = "(ссылка на файл услуг в группе проекта)", direction = "(направления проекта)" }) {
  const r = RESP.SEO;
  const out = [];
  out.push(task("Сбор семантического ядра для проекта", SEO_SEMANTICS, true, r, { направление: direction, регион: region }));
  for (let i = 1; i <= pages; i++) {
    out.push(task(`Создание ТЗ для написания текста (${i}/${pages})`, SEO_BRIEF, true, r, { предмет_ТЗ: "текста для информационной страницы" }));
  }
  out.push(task("Создание страниц", SEO_CREATE_PAGES, true, r, { N: pages, ссылка_услуг: servicesUrl }));
  out.push(task("Оптимизация показателей внутренней перелинковки", SEO_INTERLINK, true, r, {}));
  out.push(task("Закупка ссылок", SEO_BUY_LINKS, false, r, {}));
  out.push(task("Формирование ежемесячного отчета (SEO)", SEO_REPORT, false, r, { "Месяц Год": monthLabel }));
  return out;
}

/**
 * PPC-подзадачи на месяц. vars: { platforms:[label], monthLabel, region, direction }
 * Ответственный 103 + соисполнитель 17.
 */
export function ppcSubtasks({ platforms, monthLabel, region = DEFAULT_REGION, direction = "Новая рекламная активность" }) {
  const r = RESP.PPC_SUB;
  const acc = [RESP.PPC_ACCOMPLICE];
  const out = [];
  for (const p of platforms) {
    out.push(task(`Запуск рекламной активности: ${p}`, PPC_LAUNCH, true, r, { площадка: p, направление: direction, регион: region, "Месяц Год": monthLabel }, acc));
  }
  out.push(task("Формирование ежемесячного отчета (PPC)", PPC_REPORT, false, r, { "Месяц Год": monthLabel }, acc));
  return out;
}

function task(title, descrTpl, bbcode, responsibleId, vars = {}, accomplices = []) {
  return {
    title,
    description: fill(descrTpl, vars),
    bbcode,
    responsibleId,
    ...(accomplices.length ? { accomplices } : {}),
  };
}

